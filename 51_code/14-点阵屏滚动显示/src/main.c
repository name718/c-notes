#include "Com_Util.h"
#include "Int_MatrixLED.h"

// 主函数
void main()
{
    // 定义点阵屏的显示内容
    // u8 led_pixels[8] = {0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80};
    // u8 led_pixels[8] = {0x81, 0x42, 0x24, 0x18, 0x18, 0x24, 0x42, 0x81};
    // u8 led_pixels[8] = {0x00, 0x5D, 0x55, 0x55, 0x55, 0x55, 0x5D, 0x00};

    u8 i = 0;
    u8 picture[26] = {0xF8, 0x0A, 0xEC, 0xAF, 0xEC, 0x8A, 0xF8, 0x00,
                      0x10, 0xF9, 0x97, 0xF1, 0x88, 0xAA, 0xFF, 0xAA,
                      0x88, 0x00, 0x14, 0x0A, 0xF5, 0x92, 0x92, 0xF5,
                      0x0A, 0x14};

    // 初始化点阵屏
    Int_MatrixLED_Init();

    // 死循环
    while (1)
    {
        if (i < 26)
        {
            Int_MatrixLED_ShiftPixels(picture[i]);
        }
        else
        {
            Int_MatrixLED_ShiftPixels(0x00);        // 移入空行
        }

        i++;
        if (i == 34)
        {
            i = 0;
        }

        Delayms(200);
    }
}

