/*
 * 文件名：11_header_guard.c
 * 学习目标：
 * 1. 理解 #ifndef / #define / #endif 是什么
 * 2. 理解“头文件展开”到底是什么意思
 * 3. 理解为什么头文件要防止重复包含
 *
 * 一、#ifndef 是什么？
 * - #ifndef 的完整意思是：if not defined
 * - 翻译过来就是：如果某个宏还没有被定义过
 * - 它是“预处理指令”，不是普通的 C 语句
 * - 它发生在“预处理阶段”，不是程序运行阶段
 *
 * 二、什么叫“展开”？
 * - 这里说的“展开”，本质上是：预处理器对源码做文本替换、文本插入、文本裁剪
 * - 重点是“文本级别处理”，不是执行代码
 *
 * 例如：
 * 1. #include "a.h"
 *    含义不是“运行时加载 a.h”
 *    而是“把 a.h 的文本内容放到当前位置”
 *
 * 2. #define PI 3.14
 *    当代码里写了 PI
 *    预处理器会把 PI 直接替换成 3.14
 *
 * 三、为什么头文件会被重复展开？
 * - 因为一个 .c 文件可能直接包含某个头文件
 * - 也可能通过别的头文件间接包含同一个头文件
 *
 * 例子：
 *   main.c      -> #include "a.h"
 *   a.h         -> #include "student.h"
 *   main.c      -> 还可能自己再 #include "student.h"
 *
 * 这样 student.h 可能被放进 main.c 两次
 * 这就叫“重复展开”
 *
 * 四、重复展开会导致什么问题？
 * - 可能出现重复声明
 * - 可能出现重复定义
 * - 常见报错场景：同一个结构体、类型、全局变量等内容被处理了多次
 *
 * 五、头文件保护的标准写法
 *   #ifndef STUDENT_H
 *   #define STUDENT_H
 *
 *   /* 头文件真正内容 *\/
 *
 *   #endif
 *
 * 它的逻辑是：
 * 1. 如果 STUDENT_H 没定义过，就进入
 * 2. 先把 STUDENT_H 定义出来，做一个“已处理”的标记
 * 3. 保留下面的头文件内容
 * 4. 下次再遇到这个头文件时，因为 STUDENT_H 已经定义过了
 * 5. 所以下面的内容直接跳过，不再重复展开
 *
 * 六、要分清两个概念
 * - 展开：预处理器改写源码文本
 * - 运行：程序真正开始执行 main 函数后的过程
 *
 * 七、面试考点
 * - #ifndef 是什么？答：预处理条件指令，表示“如果没有定义过某个宏”
 * - 为什么要写头文件保护？答：防止头文件内容被重复展开
 * - #include 的本质是什么？答：把头文件文本插入到当前位置
 * - #pragma once 和 include guard 区别？答：前者更简洁，但属于编译器扩展；后者是更传统、更通用的写法
 */

#include <stdio.h>

int main(void)
{
    printf("头文件保护的常见写法：\n");
    printf("#ifndef MY_HEADER_H\n");
    printf("#define MY_HEADER_H\n");
    printf("/* 头文件内容，例如函数声明、结构体声明 */\n");
    printf("#endif\n\n");

    printf("关键理解：\n");
    printf("1. #ifndef 是预处理指令，不是运行时语句。\n");
    printf("2. #include 的本质是把头文件内容“展开”到当前位置。\n");
    printf("3. 所谓展开，本质是文本替换和文本插入。\n");
    printf("4. 头文件保护的作用是避免同一个头文件被重复展开。\n");

    return 0;
}
