/*
 * 文件名：16_pointers_basic.c
 * 学习目标：
 * 1. 理解指针变量保存的是地址
 * 2. 掌握取地址和解引用
 * 3. 分清“变量的值”“变量的地址”“指针里保存的地址”“指针自己的地址”
 *
 * 核心知识点：
 * - &：取地址运算符
 * - *：定义指针时表示“指针类型”，解引用时表示“访问地址里的值”
 * - %p：printf 中专门用来打印地址（指针）的格式控制符
 * - void *：通用指针类型，打印地址时常转成它
 *
 * 易错点：
 * - 指针必须指向合法地址后才能解引用
 * - int *p 和 int* p 含义一样，只是书写风格不同
 * - ptr 和 &ptr 不是一回事：前者是“ptr 里存的地址”，后者是“ptr 这个变量自己的地址”
 *
 * 面试考点：
 * - 指针的本质是什么？答：保存地址的变量
 * - * 和 & 分别是什么作用？答：一个取值，一个取地址
 * - 为什么打印地址常写成 (void *)ptr？答：因为 %p 常按 void * 处理，强转后更规范
 */

#include <stdio.h>

int main(void)
{
    /*
     * num 是一个普通 int 变量，里面存的是数值 100
     */
    int num = 100;

    /*
     * ptr 是一个“指向 int 的指针”
     * 写成 int *ptr 的意思是：
     * - ptr 的类型是指针
     * - 它应该保存一个 int 变量的地址
     *
     * 这里把 num 的地址交给 ptr：
     * - &num 表示“取 num 的地址”
     * - 所以 ptr 里保存的就是 num 的地址
     */
    int *ptr = &num;

    /*
     * 下面这几行最重要，建议你对照着理解：
     *
     * 1. num
     *    - 普通变量的值
     *
     * 2. &num
     *    - num 的地址
     *
     * 3. ptr
     *    - 指针变量里保存的地址
     *    - 因为 ptr = &num，所以 ptr 和 &num 的值通常一样
     *
     * 4. *ptr
     *    - 根据 ptr 里保存的地址，找到那个地址中的值
     *    - 因为 ptr 指向 num，所以 *ptr 就是 num 的值
     *
     * 5. &ptr
     *    - ptr 自己也是变量，它自己也有地址
     *    - 注意：&ptr 和 ptr 通常不是一个东西
     *
     * 为什么打印地址时要写 (void *)？
     * - &num 的类型是 int *
     * - ptr 的类型也是 int *
     * - &ptr 的类型是 int **
     * - %p 是打印地址的格式控制符，常把各种指针先转成 void * 再打印，更规范
     */
    printf("num 的值：%d\n", num);
    printf("&num（num 的地址） = %p\n", (void *)&num);
    printf("ptr（ptr 里保存的地址） = %p\n", (void *)ptr);
    printf("*ptr（ptr 指向地址里的值） = %d\n", *ptr);
    printf("&ptr（指针变量 ptr 自己的地址） = %p\n", (void *)&ptr);

    /*
     * 你可以重点观察：
     * - &num 和 ptr 通常打印出来一样
     * - 说明 ptr 确实保存了 num 的地址
     * - &ptr 一般会是另一个不同的地址
     * - 说明 ptr 这个变量本身也存放在某个内存位置
     */

    return 0;
}
