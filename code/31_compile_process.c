/*
 * 文件名：31_compile_process.c
 * 学习目标：
 * 1. 理解 C 程序从源码到可执行文件的大致流程
 * 2. 能回答面试中的“编译四阶段”
 *
 * 核心知识点：
 * - 预处理：处理 #include、#define、条件编译
 * - 编译：把 C 代码转换成汇编代码
 * - 汇编：把汇编代码转换成目标文件
 * - 链接：把多个目标文件和库组合成可执行文件
 *
 * 易错点：
 * - 不要把“编译”一词狭义地只理解成其中一个阶段
 * - 链接错误不等于语法错误
 *
 * 面试考点（高频）：
 * - C 的编译流程是什么？
 *   1. 预处理
 *   2. 编译
 *   3. 汇编
 *   4. 链接
 * - #include 在哪一步处理？答：预处理
 * - 未定义引用错误通常在哪一步出现？答：链接阶段
 */

#include <stdio.h>

int main(void)
{
    printf("C 编译流程：\n");
    printf("1. 预处理：展开头文件、替换宏、处理条件编译\n");
    printf("2. 编译：检查语法语义，生成汇编代码\n");
    printf("3. 汇编：生成目标文件 .o\n");
    printf("4. 链接：合并目标文件和库，生成可执行文件\n");

    return 0;
}
