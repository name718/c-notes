/*
 * 文件名：32_linking_and_symbols.c
 * 学习目标：
 * 1. 理解链接器和符号的基本作用
 * 2. 能解释静态链接、动态链接的概念
 *
 * 核心知识点：
 * - 符号可以理解为变量名、函数名等在目标文件中的“标识”
 * - 链接器负责把引用和定义匹配起来
 * - 静态链接：链接时把库代码拷贝进可执行文件
 * - 动态链接：运行时再加载共享库
 *
 * 易错点：
 * - 编译通过不代表一定能链接通过
 * - 声明了函数但没有定义，通常会在链接时报错
 *
 * 面试考点：
 * - 什么是 undefined reference？答：链接阶段找不到符号定义
 * - 静态链接和动态链接区别？答：体积、部署方式、运行依赖不同
 */

#include <stdio.h>

int main(void)
{
    printf("链接器负责把“用到的符号”和“真正定义的符号”匹配起来。\n");
    printf("静态链接：库代码进入可执行文件。\n");
    printf("动态链接：程序运行时再加载共享库。\n");

    return 0;
}
